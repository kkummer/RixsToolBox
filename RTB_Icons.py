def showIcons():
    import sys
    from PyMca5.Object3D import Object3DQt as qt

    a = qt.QApplication(sys.argv)
    a.lastWindowClosed.connect(a.quit)
    w = qt.QWidget()
    g = qt.QGridLayout(w)

    cols = 3
    for i, (name, icon) in enumerate(sorted(RtbIcons.items())):
        layout = qt.QHBoxLayout()
        lab = qt.QLabel(w)
        lab.setText(str(name))
        lab.setMinimumWidth(60)
        layout.addWidget(lab)
        lab = qt.QLabel(w)
        lab.setPixmap(qt.QPixmap(icon))
        layout.addWidget(lab)
        layout.addSpacing(100)
        IconWidget = qt.QWidget()
        IconWidget.setLayout(layout)
        g.addWidget(IconWidget, i % (len(RtbIcons.items())/cols), int(i/len(RtbIcons.items())*cols))

    w.show()
    a.exec_()

if __name__=='__main__':
    showIcons()







